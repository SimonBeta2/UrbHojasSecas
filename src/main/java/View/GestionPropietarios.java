    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(Control.gestionHS.verificarPropietario2(jTextField1.getText(),jTextField2.getText())!=null){
            
            Propietario propietarioEliminar = Control.gestionHS.verificarPropietario2(jTextField1.getText(),jTextField2.getText());
            Control.gestionHS.eliminarPropietario(propietarioEliminar.getId(),propietarioEliminar.getSalario());
            JOptionPane.showMessageDialog(null, "El propietario se elimino exitosamente");
            
            
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            
            
        }
        else{
            JOptionPane.showMessageDialog(null, "El propietario no existe");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

