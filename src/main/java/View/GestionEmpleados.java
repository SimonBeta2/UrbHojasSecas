    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(Control.gestionHS.verificarEmpleado2(jTextField1.getText(),jTextField2.getText())!=null){
            if(!jComboBox1.getSelectedItem().toString().equals("Admin")){
                Empleado empleadoEliminar = Control.gestionHS.verificarEmpleado2(jTextField1.getText(),jTextField2.getText());
                Control.gestionHS.eliminarEmpleado(empleadoEliminar.getId(),empleadoEliminar.getCargo());
                JOptionPane.showMessageDialog(null, "El empleado se elimino exitosamente");
            
            }
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jComboBox1.setSelectedIndex(0);
            JOptionPane.showMessageDialog(null, "No puede eliminar al admin");
        }
        else{
            JOptionPane.showMessageDialog(null, "El empleado no existe");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jComboBox1.setSelectedIndex(0);
        }
        

// TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

